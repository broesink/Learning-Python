### repeat a set number of times

for i in range(5):
    print(i)

for i in range(1, 5):
    print(i)

for i in range (0, 20, 5):
    print(i)

for letter in "Python":
    print(letter)

total = 0

for number in [10, 20, 30]:
    total += number
print(f"Total: {total}")